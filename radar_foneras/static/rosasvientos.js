$(function () {

    // Parse the data from an inline table using the Highcharts Data plugin
    $('#rosavientos1').highcharts({
      data: {
        table: 'freq1',
        startRow: 1,
        endRow: 14,
        endColumn: 1
      },
      chart: {
        polar: true,
        type: 'column'
      },
      
      title: {
        text: 'FONERA 1. Throughtput medio por canal [kbps]'
      },
      
      subtitle: {
        text: 'Unidades: kbps. Muestreo: 164 segundos'
      },
      
      pane: {
        size: '95%'
      },
      
      legend: {
        enabled: false,
        reversed: true,
        align: 'right',
        verticalAlign: 'top',
        y: 100,
        layout: 'vertical'
      },
      
      xAxis: {
        tickmarkPlacement: 'on'
      },

      yAxis: {
        min: 0,
        endOnTick: false,
        showLastLabel: true,
        title: {
          text: 'Throughput (kbps)'
        },
        labels: {
          formatter: function () {
            return this.value + ' kbps';
          }
        }
      },
      
      tooltip: {
        valueSuffix: ' kbps',
        followPointer: true
      },

      plotOptions: {
        series: {
          stacking: 'normal',
          shadow: false,
          groupPadding: 0,
          pointPlacement: 'on'
        }
      }
    });
});



